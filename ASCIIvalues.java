public class ASCIIvalues {
    public static void main(String[] args) {
        char a = 'a';
        char b = 'b';
        char c = 'c';

        System.out.println("ASCII value of a: " + (int) a);
        System.out.println("ASCII value of b: " + (int) b);
        System.out.println("ASCII value of c: " + (int) c);
    }
}
